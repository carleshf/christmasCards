const get_pos_of_link = ( layout, n ) => {
  n = n.toString()
  for( var ii = 0; ii < layout.length; ii++ ) {
    for( var jj = 0; jj < layout[ 0 ].length; jj ++ ) {
      if( layout[ ii ][ jj ] == n ) {
        return [ true, ii, jj ]
      }
    }
  }
  return [ false, 0, 0 ]
}

